def translate_to_lolspeak(sourcetext):
	translationtable = create_translation_table()
	outtext = ""
	for word in str(sourcetext).ToLower().Split() :
		if translationtable.ContainsKey(word):
			outtext = outtext + translationtable[word] + " "
		else:
			outtext = outtext + word + " " 
	if outtext=="" :
		return sourcetext
	else:
		return outtext

